export const transitions = {
  duration: {
    shortest: 150,
    shorter: 200,
    short: 250,
    standard: 300,
    complex: 375,
    enteringScreen: 225,
    leavingScreen: 195,
  },
  easing: {
    easeInOut: 'cubic-bezier(0.4, 0, 0.2, 1)',
    easeOut: 'cubic-bezier(0.0, 0, 0.2, 1)',
    easeIn: 'cubic-bezier(0.4, 0, 1, 1)',
    sharp: 'cubic-bezier(0.4, 0, 0.6, 1)',
  },
  create: (properties: string | string[] = ['all'], options: { duration?: number; easing?: string; delay?: number } = {}) => {
    const {
      duration = transitions.duration.standard,
      easing = transitions.easing.easeInOut,
      delay = 0,
    } = options;

    return (Array.isArray(properties) ? properties : [properties])
      .map(prop => `${prop} ${duration}ms ${easing} ${delay}ms`)
      .join(',');
  },
};

// Breakpoints
export const breakpoints: any = {
  values: {
    xs: 0,
    sm: 640,
    md: 768,
    lg: 1024,
    xl: 1280,
    '2xl': 1536,
  },
  up: (key: keyof typeof breakpoints.values) => `@media (min-width:${breakpoints.values[key]}px)`,
  down: (key: keyof typeof breakpoints.values) => `@media (max-width:${breakpoints.values[key] - 0.05}px)`,
  between: (start: keyof typeof breakpoints.values, end: keyof typeof breakpoints.values) =>
    `@media (min-width:${breakpoints.values[start]}px) and (max-width:${breakpoints.values[end] - 0.05}px)`,
  only: (key: keyof typeof breakpoints.values) => {
    const keys = Object.keys(breakpoints.values) as Array<keyof typeof breakpoints.values>;
    const index = keys.indexOf(key);
    const nextKey = keys[index + 1];

    if (index === keys.length - 1) {
      return breakpoints.up(key);
    }

    return breakpoints.between(key, nextKey);
  },
};

